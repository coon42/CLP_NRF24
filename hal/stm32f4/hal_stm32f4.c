void delayUs(uint32_t microseconds) {
  int i;
  volatile uint16_t cur = 0;
  uint32_t parts = microseconds / UINT16_MAX; // for values bigger than 16 bit ...
  uint32_t rest_us = microseconds % UINT16_MAX;

  for(i=0; i < 2 * parts; i++) {
    cur = 0;
    TIM3->CNT = 0;

    while( cur < UINT16_MAX / 2 ) {
      cur = TIM3->CNT;
    }
  }

  cur = 0;
  TIM3->CNT = 0;

  while( cur < rest_us ) {
	  cur = TIM3->CNT;
  }
}

